package OverLoadFuntion;

public class Caculator {
    public static double sum(double a, double b){
        return a + b;
    }
    public static double sum(double a,double b, double c){
        return a+b+c;
    }
    public static double sum(double a, double b, double c, double d){
        return a+b+c+d;
    }
}
